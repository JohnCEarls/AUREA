                    </ul>
                    <li>Python 2.6 or 2.7</li>
                    <ul>
                        <li><em>Note: you will also need the dev libraries. Should be available from any package manager.(python-dev)</em></li>
                        <li><em>Note: You will also need the python-tk libraries.</em></li>
                    </ul>
                </ul>
            </div>

