					<h2>Build AUREA from source</h2>

<p>
So, you want to build from source?  Abandon hope all ye who enter here.
                <ul>
                    <li><a class="textlink"  href="/AUREA/build.php#source">Get the source</a>
                    </li><li><a class="textlink"  href="/AUREA/build.php#mac">Build on Mac OS X</a>
                    </li><li><a class="textlink"  href="/AUREA/build.php#win">Build on Windows</a>

                    </li><li><a class="textlink"  href="/AUREA/build.php#lin">Build on Linux</a>
                </li></ul>
            </div>
            <h3>
                <a name="source">Get the source</a>
            </h3>
            <div>
                The full source code is available at <a class="textlink"  href="https://github.com/JohnCEarls/AUREA">https://github.com/JohnCEarls/AUREA</a>.
                If you have <a class="textlink"  href="http://git-scm.com/">git</a> installed you can navigate via the command line to where you want to perform your build and enter:
                <ul>

                <li>git clone git://github.com/JohnCEarls/AUREA.git</a>
                </ul>
                This will put the latest version of the source code into a folder name AUREA.
                <p>If you do not have git installed, you can click the Download/ZIP button (at  <a class="textlink"  href="https://github.com/JohnCEarls/AUREA">https://github.com/JohnCEarls/AUREA</a>) and download a zipped version of the AUREA build libraries.  Simply unzip that folder wherever you want to build the source.</p>
            </div>
            <h3>
                <a name="mac">
                Build on Mac OSX
                </a>
            </h3>
            <div>

                <strong>Prereqs:</strong>
                <ul>
                    <li>Python &gt;= 2.6
                        <ul>
                            <li>2.6.x is default on OS X Snow Leopard and up.</li>
                            <li>Installing XCode on Leopard and earlier should upgrade Python to 2.7.x</li>
                        </ul>
                    </li>

                    
                    <li><a class="textlink"  href="http://developer.apple.com/xcode/">XCode</a> 
                        <ul>
                            <li>This will install the GNU toolchain and SWIG packages</li>
                            <li>Supposedly XCode 3 is available for download for free somewhere.(If you find out where, please email john.c.earls-at-gmail and I will add a link here.)</li>
                            <li>XCode should come with any OS X installation disks.</li>
                            <li>XCode 4 is available at the Mac App Store for $5.99</li>
                        </ul>

                    
                    </li>
        
                    <li>
                        Python <a class="textlink"  href="http://pypi.python.org/pypi/setuptools#cygwin-mac-os-x-linux-other">Easy_Install</a> tools
                        <ul>
                            <li>This may come with the default python installation.</li>
                        </ul>
                    </li>

                    <li>
                        Tkinter
                        <ul>
                            <li>Should be already installed</li>
                        </ul>

                    </li>
                    
                </ul>

            </div>

            <div>
               <strong> Build instructions</strong>
                <ul>
                    <li>Open terminal</li>
                    <li>Navigate to the base AUREA directory</li>
                    <li>Options from here:
                        <ul>
                            <li>Install from source
                                <ul>

                                    <li>enter: <strong>python setup.py install</strong></li>
                                </ul>
                            </li>
                            <li>Build a binary <a class="textlink"  href="http://www.ibm.com/developerworks/linux/library/l-cppeak3/index.html">egg</a> distribution without installing libraries
                                <ul>
                                    <li>enter: <strong>ARCHFLAGS="-arch i386 -arch x86_64" python setup.py bdist_egg</strong>

                                    <ul>
                                        <li><em>Note: the ARCHFLAGS argument is necessary with XCode 4 (see "<a class="textlink"  href="http://superuser.com/questions/259278/python-2-6-1-pycrypto-2-3-pypi-package-broken-pipe-during-build">broken pipe during build</a>")</em></li>
                                    </ul>
                                    </li>
                                </ul>
                            </li>
                            <li>Build a <a class="textlink"  href="http://docs.python.org/distutils/builtdist.html">dumb</a> binary distribution without installing libraries
                                <ul>

                                    <li>enter: <strong>ARCHFLAGS="-arch i386 -arch x86_64" python setup.py bdist</strong>
                                       <ul>
                                            <li><em>Note: the ARCHFLAGS argument is necessary with XCode 4 (see "<a class="textlink"  href="http://superuser.com/questions/259278/python-2-6-1-pycrypto-2-3-pypi-package-broken-pipe-during-build">broken pipe during build</a>")</em></li>
                                        </ul>
                                    </li>
                               </ul>

                            </li>
                        </ul>
                    </li>
                <li>Install the <a href="/AUREA/install.php#macwork">workspace</a>.</li>
                    
     
                </ul>
            </div>
            <h3>
                <a name="win">
                Build on Windows
                </a>
            </h3>

            <div>
                <strong>Prereqs:</strong>
                <ul>
                    <li>Visual Studio 2008</li>
                        <ul><li><em>Note: VS 2010 is possible, but the python libraries are built using 2008 and it gets messy, see http://nukeit.org/compile-python-2-7-packages-with-visual-studio-2010-express)</em></li></ul>
                     <li>Python 2.6 or 2.7</li>
                    <ul>
                        
                        <li>Must add python to the Path and 2 environmental variables (<a href="http://www.microsoft.com/resources/documentation/windows/xp/all/proddocs/en-us/environment_variables.mspx?mfr=true">winxp</a> / <a href="http://www.windows7hacker.com/index.php/2010/05/how-to-addedit-environment-variables-in-windows-7/">Windows 7</a>) User variables are fine for PYTHON_INCLUDE and PYTHON_LIB.</li>
                        <ul>
                            <li>The python directory must be added to the <strong>Path</strong> system environmental variable (C:\Python26 for example)</li>
                            <li>PYTHON_INCLUDE - which points at the include folder in the Python2X folder (C:\Python26\include for example)</li>
                            <li>PYTHON_LIB - which points at the file python2X.lib (C:\Python26\libs\python26.lib for example</li>
                        </ul>
                    </ul>
                    <li>Swig (<a href="http://sourceforge.net/projects/swig/files/swigwin/swigwin-2.0.1/">Swigwin at SF</a>)</li>
                    <ul>
                        <li>Must add swig to Path (see above for how to change Environmental variables) <em>Note: You cannot just add the executable, the compiler will need all of the files in the swig folder.</em></li>
                    </ul>
                </ul>
            

            </div>
            <div>
                <strong>Build instructions</strong> <br/> 
                <em>Note: this is the easy part, getting the pre-reqs right is the painful part</em>
                <ul>
                    <li>Get the <a href="/AUREA/build.php#source">source</a> either using "git pull" or by downloading the zip file from github.</li>
                    <li>
                        Open Powershell or Command Prompt.
                    </li>
                    <li>
                        Navigate to the unpackaged AUREA directory. (cd "C:\Documents and Settings\user\AUREA" for example)
                    </li>
                    <li>Now you can either build the files, install the library or make distributable packages</li>
                    <ul>
                    <li>
                       Install from source
                    </li>
                    <ul>
                    <li>
                        enter <strong>python setup.py install</strong>
                    </li>
                    </ul>
                    <li>
Build a binary  <a class="textlink"  href="http://www.ibm.com/developerworks/linux/library/l-cppeak3/index.html">egg</a> distribution without installing libraries
                    </li>
                    <ul>
                    <li>
                        enter <strong>python setup.py bdist_egg</strong>
                    </li>
                    </ul>
                    <li>
                        Build a windows installer distribution without installing libraries
                    </li>
                    <ul>
                    <li>
                        enter <strong>python setup.py bdist_wininst</strong>
                    </li>
                    </ul>
                </ul>
                
                <li>Install the <a href="/AUREA/install.php#winwork">workspace</a>.</li>
            </ul>
            </div>
