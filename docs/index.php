<p>Relative Expression Analysis, which is based on the relative 
ordering of expression values for a small number of genes, 
has been shown in several studies to accurately classify between 
disease phenotypes, cancer subclasses, disease outcomes and diverse 
human pathologies assayed through blood-borne leukocytes
The TSP family of Relative Expression Analysis algorithms possess 
similar accuracy in classification tasks to other supervised 
learning based classifiers such as Support Vector Machines; 
but they also provide an important advantage over many classical 
methods of discrimination: interpretability.</p>

<p>
The ability to leverage insight provided by the returned gene pairs
(or triplets or networks) into tasks beyond classification, such as 
the exploration of their use as possible drug targets or as clues to 
the underlying biological nature of a phenotype, make these algorithms 
powerful tools for scientific exploration.</p>

<p>
AUREA presents four algorithms from this family of computational methods.
The Top Scoring Pair(TSP) algorithm attempts to find the pair of genes with the
 maximum likelihood of being ordered consistently within each class but 
differently between classes.
The k-TSP algorithm attempts to find the set of k or less disjoint TSPs with 
the greatest accuracy based on internal cross-validation.
The Top Scoring Triplet(TST) algorithm attempts to find the triplet of genes 
with the maximum likelihood of being ordered consistently within each class
but differently between given classes.
The Differential Rank Conservation algorithm (DiRaC) attempts to find the gene 
network with maximum likelihood of showing consistent relative 
expression(i.e. ranking of network genes) within a class while displaying 
different relative expression between given classes.
</p>
<p>
Currently, several excellent versions of these algorithms are available for download;  
DiRaC is available for Matlab, k-TSP is available as a Perl script,
and TSP and TST are available in both R scripts and as Matlab scripts for 
GPU architectures.
Despite their excellence(C code from one of these and ideas from all are
incorporated into AUREA), these implementations have serious shortcomings 
in the disparate platforms they are available for, 
their accessibility to individuals 
untrained in the programming languages they were developed and in the 
subset of learning algorithms provided.
As a cross platform, open source combination of all of these algorithms 
that can be used without any programming experience,
we believe that AUREA opens these methods to be fully utilized and explored
by the entire biological community.
</p>
<p>
A primary challenge in effectively using these algorithms is selecting 
the model where the genomic features 
learned by the model are most reflective of the provided classification problem.
Each algorithm has its own set of parameters, over which the accuracy 
of the models selected features can fluctuate within and between datasets.
For example, TSP, k-TSP and TST filter the input data to learn only on 
a specified number of the most differentially expressed genes in order to 
both reduce computational complexity and to constrain the space of genes 
examined to candidates where expression diverges between phenotypes.
In total, for these four algorithms, there are 12 parameters many of which have thousands of possible values.
These values have a dramatic effect on the time and space requirements of the model; 
e.g., choosing to run TST on all genes in a microarray (~10k) requires several 
terabytes of memory and 6.5 months on a single CPU.
</p>
<p>

AUREA allows a user to adaptively search the model space to find a model with 
learned features that provide the highest apparent accuracy over a training set, 
while weighing the computational complexity of the model.
By simply specifying a target accuracy and a maximum running time, AUREA removes the necessity of a time-consuming manual search of the model space. 
Importantly, AUREA is designed with an easy to use Graphical User Interface, 
lowering the technical barriers to exploiting the rich research opportunities of these models.</p>

